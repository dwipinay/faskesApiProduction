import { getDataPm, getDataDokumen, getDataAlkes, getDataPembiayaan, getDataPrognas, getDataHipertensi,getDataOhis } from '../models/PraktekMandiriInmModel.js'
import paginationDB from '../config/PaginationDB.js'
import Joi from 'joi'

export const showDataDasar = (req, res) => {
    getDataPm(req.params.id, (err, results) => {
        if (err) {
            res.status(422).send({
                status: false,
                message: err
            })
            return
        }

        const message = results.length ? 'data found' : 'data not found'
        const data = results.length ? results[0] : null

        res.status(200).send({
            status: true,
            message: message,
            data: data
        })
    })
}

export const showDataDokumen = (req, res) => {
    const schema = Joi.object({
        kodeFaskes: Joi.string(),
        page: Joi.number(),
        limit: Joi.number()
    })

    const { error, value } =  schema.validate(req.query)

    if (error) {
        res.status(400).send({
            status: false,
            message: error.details[0].message
        })
        return
    }
    
    getDataDokumen(req, (err, results) => {
        if (err) {
            res.status(422).send({
                status: false,
                message: err
            })
            return
        }

        const paginationDBObject = new paginationDB(results.totalRowCount, results.page, results.limit, results.data)
        const remarkPagination = paginationDBObject.getRemarkPagination()
        const message = results.data.length ? 'data found' : 'data not found'

        res.status(200).send({
            status: true,
            message: message,
            pagination: remarkPagination,
            data: results.data
        })
    })
}

